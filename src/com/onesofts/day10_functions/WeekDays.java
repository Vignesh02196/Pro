package com.onesofts.day10_functions;

public class WeekDays {
	public void findTheDay(String day){
		switch(day)
		{
		case "Sunday":System.out.println("Given day is first day");break;
		case "Monday":System.out.println("Given day is second day");break;
		case "Tuesday":System.out.println("Given day is third day");break;
		case "wednesday":System.out.println("Given day is fourth day");break;
		case "thursday":System.out.println("Given day is fifth day");break;
		case "friday":System.out.println("Given day is sixth day");break;
		case "Saturday":System.out.println("Given day is Seventh day");break;
		default:System.out.println("UNvalid data");
		}
		}
		
	}
	


